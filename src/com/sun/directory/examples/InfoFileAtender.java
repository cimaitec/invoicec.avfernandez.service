package com.sun.directory.examples;

public class InfoFileAtender {	
	
	private String fileName;
	private String ambiente;
	private String rucFile;
	private String tipoDocumento;
	private String codEstablecimiento;
	private String codPuntEmision;
	private String secuencial;
    
	public static void getInfoFileAteneder(){
		
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getAmbiente() {
		return ambiente;
	}

	public void setAmbiente(String ambiente) {
		this.ambiente = ambiente;
	}

	public String getRucFile() {
		return rucFile;
	}

	public void setRucFile(String rucFile) {
		this.rucFile = rucFile;
	}

	public String getTipoDocumento() {
		return tipoDocumento;
	}

	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}

	public String getCodEstablecimiento() {
		return codEstablecimiento;
	}

	public void setCodEstablecimiento(String codEstablecimiento) {
		this.codEstablecimiento = codEstablecimiento;
	}

	public String getCodPuntEmision() {
		return codPuntEmision;
	}

	public void setCodPuntEmision(String codPuntEmision) {
		this.codPuntEmision = codPuntEmision;
	}

	public String getSecuencial() {
		return secuencial;
	}

	public void setSecuencial(String secuencial) {
		this.secuencial = secuencial;
	}
	
	
	
}
